// Listeners para cambio de idioma
document.getElementById('btn-es').onclick = () => cambiarIdioma('es');
document.getElementById('btn-en').onclick = () => cambiarIdioma('en');

function cambiarIdioma(idioma) {
  document.getElementById('frase').innerText = textos[idioma].frase;
  document.getElementById('cv-link').innerText = textos[idioma].botonCv;
  document.getElementById('sobre-mi-titulo').innerText = textos[idioma].sobreMiTitulo;
  document.getElementById('sobre-mi-texto').innerHTML = textos[idioma].sobreMiTexto;
  document.getElementById('logros-titulo').innerText = textos[idioma].logrosTitulo;
  document.getElementById('certificados-titulo').innerText = textos[idioma].certificadosTitulo;
  document.getElementById('cartas-titulo').innerText = textos[idioma].cartasTitulo;
  document.getElementById('contacto-titulo').innerText = textos[idioma].contactoTitulo;
  document.getElementById('frase-contacto').innerText = textos[idioma].fraseContacto;
  document.getElementById('experiencia-titulo').innerText = textos[idioma].experienciaTitulo;
  document.getElementById('experiencia-contenido').innerHTML = textos[idioma].experienciaContenido;

  // Logros (Achievements)
  const lista = document.getElementById('logros-lista');
  lista.innerHTML = '';
  textos[idioma].logrosLista.forEach(item => {
    const li = document.createElement('li');
    li.innerText = item;
    lista.appendChild(li);
  });
}

// Idioma predeterminado: inglés
cambiarIdioma('en');
